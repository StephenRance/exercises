import { Button, Filter, Filters, Header, Main } from '@/components';
import { BodyArea as FilterProps } from '@/types';
import { getExercises, getFilters, replaceImages } from '@/utils';
import Head from 'next/head';

type Props = {
  filters: FilterProps[];
};

const Home = ({ filters }: Props) => {
  return (
    <>
      <Head>
        <title>Exercises! by Stephen Rance</title>

        <meta content="Generated by create next app" name="description" />
        <meta content="width=device-width, initial-scale=1" name="viewport" />

        <link href="/favicon.png" rel="icon" />
        <link
          as="font"
          crossOrigin="anonymous"
          href="/fonts/roboto-bold.woff2"
          rel="preload"
          type="font/woff2"
        />
        <link
          as="font"
          crossOrigin="anonymous"
          href="/fonts/roboto-regular.woff2"
          rel="preload"
          type="font/woff2"
        />
      </Head>

      <Header />

      <Main>
        <Filters>
          {filters.map((filter, i) => (
            <Filter key={i}>
              <Button label={filter} />
            </Filter>
          ))}
        </Filters>
      </Main>
    </>
  );
};

Home.getInitialProps = async () => {
  let exercises = await getExercises();
  exercises = await replaceImages(exercises);

  const filters = await getFilters(exercises);

  return {
    filters,
  };
};

export default Home;
